/**
 * Converts simplified world GeoJSON (equirectangular) to SVG path `d` strings
 * matching MAP_VIEW_W=1000, MAP_VIEW_H=500 in mapProjections.ts
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const MAP_W = 1000;
const MAP_H = 500;

const USA_BOUNDS = { lonMin: -125.2, lonMax: -66.2, latMin: 24.3, latMax: 49.4 };

function projectWorld(lon, lat) {
  const x = ((lon + 180) / 360) * MAP_W;
  const y = ((90 - lat) / 180) * MAP_H;
  return [Math.round(x * 100) / 100, Math.round(y * 100) / 100];
}

/** Match mapProjections.projectUsa — CONUS in full viewBox. */
function projectUsa(lon, lat) {
  const { lonMin, lonMax, latMin, latMax } = USA_BOUNDS;
  const x = ((lon - lonMin) / (lonMax - lonMin)) * MAP_W;
  const y = ((latMax - lat) / (latMax - latMin)) * MAP_H;
  return [Math.round(x * 100) / 100, Math.round(y * 100) / 100];
}

function ringToD(ring, project) {
  if (!ring || ring.length < 2) return "";
  const [x0, y0] = project(ring[0][0], ring[0][1]);
  let d = `M ${x0} ${y0}`;
  for (let i = 1; i < ring.length; i++) {
    const [x, y] = project(ring[i][0], ring[i][1]);
    d += ` L ${x} ${y}`;
  }
  d += " Z";
  return d;
}

function polygonCoordsToD(coords, project) {
  if (!coords || !coords.length) return "";
  return coords.map((ring) => ringToD(ring, project)).filter(Boolean).join(" ");
}

function geometryToD(geom, project) {
  if (!geom) return "";
  if (geom.type === "Polygon") {
    return polygonCoordsToD(geom.coordinates, project);
  }
  if (geom.type === "MultiPolygon") {
    return geom.coordinates
      .map((polygon) => polygonCoordsToD(polygon, project))
      .filter(Boolean)
      .join(" ");
  }
  return "";
}

const input = process.argv[2] || path.join(ROOT, "world-simp.geojson");
const raw = fs.readFileSync(input, "utf8");
const geo = JSON.parse(raw);

const worldParts = [];
let usaD = "";

for (const f of geo.features) {
  const id = f.id ?? f.properties?.id;
  const name = f.properties?.name;
  if (name === "Antarctica") {
    continue;
  }
  const dWorld = geometryToD(f.geometry, projectWorld);
  if (dWorld) worldParts.push(dWorld);
  if (id === "USA" || name === "USA") {
    const g = f.geometry;
    if (g?.type === "MultiPolygon" && g.coordinates.length > 0) {
      // Lower 48: largest polygon in Natural Earth; AK/HI need different framing for CONUS view.
      const conus = { type: "MultiPolygon", coordinates: [g.coordinates[0]] };
      const dUsa = geometryToD(conus, projectUsa);
      if (dUsa) usaD = dUsa;
    } else if (g) {
      const dUsa = geometryToD(g, projectUsa);
      if (dUsa) usaD = dUsa;
    }
  }
}

const worldD = worldParts.join(" ");

const outPath = path.join(ROOT, "src", "widgets", "generated", "mapPaths.ts");
fs.mkdirSync(path.dirname(outPath), { recursive: true });
const out = `/* Auto-generated by scripts/build-map-paths.mjs from Natural Earth–style GeoJSON (simplified). */

export const WORLD_MAP_PATH_D = ${JSON.stringify(worldD)};

export const USA_MAP_PATH_D = ${JSON.stringify(usaD)};
`;
fs.writeFileSync(outPath, out, "utf8");
console.log("Wrote", outPath, "world len", worldD.length, "usa len", usaD.length);
